export function add(slumNumber, diffslumNumber) {
    return slumNumber + diffslumNumber;
}
export function subtract(slumNumber, diffslumNumber) {
   return slumNumber - diffslumNumber;
}
export function multiply(slumNumber, diffslumNumber) {
    return slumNumber * diffslumNumber;
}
export function divide(slumNumber, diffslumNumber) {
    return slumNumber / diffslumNumber;
}